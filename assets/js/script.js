document.addEventListener('DOMContentLoaded', function () {
    document.getElementById('see-all').addEventListener('click', function () {
        var artistsContainer = document.getElementById('artists-container');
        if (artistsContainer.style.display === 'none' || artistsContainer.style.display === '') {
            artistsContainer.style.display = 'block';
        } else {
            artistsContainer.style.display = 'none';
        }
    });
});

